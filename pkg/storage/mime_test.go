package storage

import (
	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
	"net/mail"
	"strings"
	"testing"
)

func TestNewMail(t *testing.T) {
	cases := []struct {
		message string
		check   func(t *testing.T, mail *Mail)
	}{
		{
			message: simpleMail,
			check: func(t *testing.T, mail1 *Mail) {
				assert.Equal(t, "Test Mail", mail1.Subject)
				assert.Equal(t, "text/plain", mail1.Body.ContentType)
				assert.Equal(t, []byte("Hi\n"), mail1.Body.Data)
			},
		},
		{
			message: mailWithInlineResources,
			check: func(t *testing.T, mail1 *Mail) {
				assert.Equal(t, "Test HTML email with JPG image.", mail1.Subject)
				assert.Equal(t, "text/html", mail1.Body.ContentType)
				assert.Equal(t, 1, len(mail1.Body.Embeds))
				assert.Equal(t, "image/jpeg", mail1.Body.Embeds[0].ContentType)
				assert.NotNil(t, mail1.Body.Embeds[0].Data)
			},
		},
		{
			message: mailWithAlternativeBody,
			check: func(t *testing.T, mail1 *Mail) {
				assert.Equal(t, "Foooo", mail1.Subject)
				assert.Equal(t, "text/html", mail1.Body.ContentType)
				assert.Equal(t, 1, len(mail1.Body.Embeds))
				assert.Equal(t, "image/png", mail1.Body.Embeds[0].ContentType)
				assert.NotNil(t, mail1.Body.Embeds[0].Data)
			},
		},
		{
			message: mailWithAttachment,
			check: func(t *testing.T, mail1 *Mail) {
				assert.Equal(t, "Test with Attachment", mail1.Subject)
				assert.Equal(t, "text/plain", mail1.Body.ContentType)
				assert.Equal(t, 1, len(mail1.Body.Attachments))
				assert.Equal(t, "image/gif", mail1.Body.Attachments[0].ContentType)
				assert.NotNil(t, mail1.Body.Attachments[0].Data)
			},
		},
		{
			message: mailWithAttachment1,
			check: func(t *testing.T, mail1 *Mail) {
				assert.Equal(t, "Fwd: Test with Attachment", mail1.Subject)
				assert.Equal(t, "text/html", mail1.Body.ContentType)
				assert.Equal(t, 1, len(mail1.Body.Attachments))
				assert.Equal(t, "text/xml", mail1.Body.Attachments[0].ContentType)
				assert.NotNil(t, mail1.Body.Attachments[0].Data)
			},
		},
	}
	for _, test := range cases {
		msg, err := mail.ReadMessage(strings.NewReader(test.message))
		require.NoError(t, err)
		mail1, err := NewMail(msg)
		require.NoError(t, err)
		test.check(t, mail1)
	}
}

